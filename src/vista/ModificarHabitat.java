    private void jModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jModificarActionPerformed
      id = Integer.parseInt(jId.getText());
        String NombreH = null;
        String descripcion = null;

        if (!jZona.getText().equals("")){
            NombreH = jZona.getText();
        }

        if (!jDescripcion.getText().equals("")){
            descripcion = jDescripcion.getText();
        }


       // NombreH = jZona.getText();
//        descripcion = jDescripcion.getText();
        

        ControlHabitat objm = new ControlHabitat();

        Habitat objh=new Habitat(NombreH, descripcion);

        try {
            boolean ch = objm.ModificarHabitats(objh, id);
            if (ch){
                System.out.println("Se modificó habitats");
            } else {
                System.out.println("No se modificó");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jModificarActionPerformed

