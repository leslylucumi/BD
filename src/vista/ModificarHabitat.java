    private void jAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAceptarActionPerformed
        

        try {

            ControlHabitat objc = new ControlHabitat();
            id = Integer.parseInt(jId.getText());
            Habitat objcu = objc.ModificarHabitat(id);
            jZona.setText(objcu.getNombreH());
            jDescripcion.setText(objcu.getDescripcion());
            

            jZona.setEnabled(true);
            jDescripcion.setEnabled(true);
         

        } catch (Exception e) {
            System.out.println("error" + e);
            e.printStackTrace();
        }
                                         
    
    }//GEN-LAST:event_jAceptarActionPerformed

